package dataJson;

public class Location {
    float latitude;
    float longitude;
    String city;
    String country;

    public float getLatitude() {
        return latitude;
    }

    public float getLongitude() {
        return longitude;
    }

    public String getCity() {
        return city;
    }

    public String getCountry() {
        return country;
    }
}

